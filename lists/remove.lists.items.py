#Remove List Items:

"""
Remove Specified Item

The remove() method removes the specified item.
"""

#Example:

thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)


#Example: 
"""
Remove the first occurrence of "banana" 
"""

thislist = ["apple", "banana", "cherry", "banana", "kiwi"]
thislist.remove("banana")
print(thislist)


